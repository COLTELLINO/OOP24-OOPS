package it.unibo.oop.utils;

/**
 * Represents the possible states of the game.
 */
public enum GameState {
    /** Title screen. */
    TITLESTATE,
    /** Options menu from title. */
    TITLEOPTIONSTATE,
    /** Main gameplay. */
    PLAYSTATE,
    /** Pause menu. */
    PAUSEMENU,
    /** Game over screen. */
    GAMEOVER
}
